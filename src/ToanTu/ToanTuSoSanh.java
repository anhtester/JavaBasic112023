package ToanTu;

public class ToanTuSoSanh {
    public static void main(String[] args) {

        int a = 10;
        int b = 20;
        int c = 10;

        System.out.println(a == b); //So sánh a có BẰNG b hay không
        System.out.println(a != b); //So sánh a có KHÁC b hay không
        System.out.println(a >= c); //So sánh a lớn hơn HOẶC bằng c hay không
        System.out.println(a <= b);
    }
}
